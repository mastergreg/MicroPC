INCLUDE MACROS.TXT

STACK_SEG SEGMENT STACK
    DW 128 DUP(?)
ENDS


DATA_SEG SEGMENT
    MSG DB "GIMME <=20 CHARS END PRESS RETURN '/' TO QUIT$"
ENDS

CODE_SEG SEGMENT
    ASSUME CS:CODE_SEG,SS:STACK_SEG,DS:DATA_SEG,ES:DATA_SEG

MAIN PROC FAR
;FOR SEGMENT REGISTERS
    MOV AX,DATA_SEG
    MOV DS,AX
    MOV ES,AX
    CALL GET_INPUT
    PRINT_NUM DL

START:
    PRINT_STRING MSG

EX:
    EXIT
MAIN ENDP

GET_INPUT PROC NEAR
    MOV DX,0
    MOV CX,20
READL:
    READ
    CMP AL,'/'
    JE EX
    SUB AL,30H ;FOR NUMBERS PUT THEM IN DX
    MOV AH,0   
    MOV BX,AX
    MOV AX,10
    MUL DX
    MOV DX,AX
    MOV AX,BX
    ADD DX,AX
    LOOP READL
    RET
GET_INPUT ENDP


CODE_SEG ENDS

END MAIN
