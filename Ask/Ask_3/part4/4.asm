INCLUDE MACROS.TXT

STACK_SEG SEGMENT STACK
    DW 128 DUP(?)
ENDS


DATA_SEG SEGMENT
    MSG DB "GIMME <=20 CHARS END PRESS RETURN '/' TO QUIT",0AH,0DH,"$"  
    SPACE DB " "
    LINE DB 0AH,0DH,"$"       
    NUMS DB 20 DUP("$")
    NCNT DW 0
    LOWC DB 20 DUP("$")
    LCNT DW 0
    UPRC DB 20 DUP("$")
    UCNT DW 0
    
    
ENDS

CODE_SEG SEGMENT
    ASSUME CS:CODE_SEG,SS:STACK_SEG,DS:DATA_SEG,ES:DATA_SEG

MAIN PROC FAR
    CALL GET_INPUT
    EXIT
MAIN ENDP

GET_INPUT PROC NEAR
    GETHON CX
    GETHON BX
    GETHON DI
    GETHON SI
    RET
ENDP GET_INPUT

GETHEX PROC NEAR
R:  READ
    MOV AH,0
    CMP AL,30H ;0
    JL R
    CMP AL,40H ;9+1
    JL NUM
    CMP AL,41H ;A
    JL R
    CMP AL,47H ;F+1
    JL CAPS
    CMP AL,61H ;a
    JL  R
    CMP AL,67H ;f+1
    JL  SMALL
    JMP R
NUM:
    SUB AL,30H
    RET
CAPS:
    SUB AL,37H
    RET
SMALL:
    SUB AL,57H
    RET
    




CODE_SEG ENDS

END MAIN
